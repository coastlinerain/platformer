pub struct Map {
    pub grids: Vec<Vec<Vec<Vec<TileType>>>>,
    pub data: Vec<Vec<Vec<Vec<EntityType>>>>,
}

impl Map {
    pub fn new() -> Self {
        let grids = Self::get_world_matrix();
        let data = Self::get_world_data();
        Self { grids, data }
    }

    fn get_world_matrix() -> Vec<Vec<Vec<Vec<TileType>>>> {
        // Función auxiliar interna para no repetir código
        fn convert(raw_layout: [[u8; MAP_WIDTH]; MAP_HEIGHT]) -> Vec<Vec<TileType>> {
            raw_layout
                .iter()
                .map(|row| {
                    row.iter()
                        .map(|&value| TileType::from(value)) // Convertimos u8 a TileType
                        .collect()
                })
                .collect()
        }

        let row0 = vec![convert(LVL_0_0), convert(LVL_0_1)];
        let row1 = vec![convert(LVL_1_0), convert(LVL_1_1)];

        vec![row0, row1]
    }

    fn get_world_data() -> Vec<Vec<Vec<Vec<EntityType>>>> {
        // Función auxiliar interna para no repetir código
        fn convert(raw_layout: [[u8; MAP_WIDTH]; MAP_HEIGHT]) -> Vec<Vec<EntityType>> {
            raw_layout
                .iter()
                .map(|row| {
                    row.iter()
                        .map(|&value| EntityType::from(value)) // Convertimos u8 a EntityType
                        .collect()
                })
                .collect()
        }

        let row0 = vec![convert(DATA_0_0), convert(DATA_0_1)];
        let row1 = vec![convert(DATA_1_0), convert(DATA_1_1)];

        vec![row0, row1]
    }
}
